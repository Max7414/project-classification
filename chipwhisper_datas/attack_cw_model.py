if __name__ == "__main__":
    # 1. 載入資料
    print("[*] 載入資料中...")
    traces = np.load("data/cw_traces.npy")
    textins = np.load("data/cw_textins.npy")
    keys = np.load("data/cw_keys.npy")
    
    # 2. 確保與訓練時相同的標準化 (Z-Score Normalization)
    print("[*] 執行波形標準化 (防除零版本)...")
    mean_val = np.mean(traces, axis=0)
    std_val = np.std(traces, axis=0)
    
    # 將標準差為 0 的地方強制設為 1，避免發生除以零的 NaN 災難
    std_val[std_val == 0] = 1 
    
    traces = (traces - mean_val) / std_val 
    X = traces.reshape((traces.shape[0], traces.shape[1], 1))    
    # 3. 擷取「測試集」
    # 訓練時我們用了前 80%，所以攻擊時我們使用最後的 20% (保證 AI 沒看過這些波形)
    split = int(len(X) * 0.8)
    X_attack = X[split:]
    textins_attack = textins[split:]
    keys_attack = keys[split:]
    
    NUM_TRACES = len(X_attack)
    TRUE_KEY = keys_attack[0][0] # 取得第一條波形的第一個 Byte 作為真實答案
    print(f"[*] 準備使用 {NUM_TRACES} 條未見過的波形進行攻擊...")
    print(f"[*] 目標真實金鑰為: 0x{TRUE_KEY:02X}")

    # 4. 載入訓練好的模型
    print("[*] 載入 Zaid 論文架構模型...")
    model = load_model("data/zaid_cw_model.h5")

    # 5. 讓 AI 推論出 256 個分類的機率
    print("[*] AI 正在推論每條波形的特徵...")
    predictions = model.predict(X_attack)

    # 6. 開始進行證據累積 (Guessing Entropy)
    print("[*] 開始進行側信道對數似然計算 (Log-Likelihood)...")
    key_log_probs = np.zeros(256)
    guessing_entropy = []

    for i in tqdm(range(NUM_TRACES), desc="攻擊進度"):
        # 取出該條波形的明文 (第一 Byte) 與預測機率
        p = textins_attack[i][0]
        pred_probs = predictions[i]
        
        # 暴力猜測 256 種金鑰
        for k_guess in range(256):
            # 如果金鑰是 k_guess，那 S-Box 輸出的值會是多少？
            hyp_sbox_out = SBOX[p ^ k_guess]
            
            # 將 AI 對於這個 S-Box 輸出的信心指數 (機率) 加上去
            # 加上 1e-36 避免 numpy 在計算 log(0) 時出現無限大錯誤
            key_log_probs[k_guess] += np.log(pred_probs[hyp_sbox_out] + 1e-36)
        
        # 根據累積的分數，對 256 種可能的金鑰由大到小排名
        ranked_keys = np.argsort(key_log_probs)[::-1]
        
        # 找出「真正的金鑰」目前排在第幾名 (0 代表第一名，破解成功)
        rank = np.where(ranked_keys == TRUE_KEY)[0][0]
        guessing_entropy.append(rank)

    # 7. 輸出火力展示圖
    print(f"\n[*] 攻擊結束！最終真實金鑰排名: {guessing_entropy[-1]} (如果是 0 代表完全破解！)")
    
    plt.figure(figsize=(10, 6))
    plt.plot(guessing_entropy, color='red', linewidth=2, label="Zaid Model Attack")
    plt.title(f'CW-Lite STM32 SCA Attack (Test Traces: {NUM_TRACES})')
    plt.xlabel('Number of Traces')
    plt.ylabel('Guessing Entropy (Rank of True Key)')
    plt.legend()
    plt.grid(True)
    
    save_path = "data/cw_attack_result.png"
    plt.savefig(save_path)
    print(f"[*] 破解曲線圖已儲存至: {save_path}")
