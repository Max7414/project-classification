def build_zaid_model(input_length):
    inputs = Input(shape=(input_length, 1))
    x = Conv1D(filters=2, kernel_size=1, activation='selu', kernel_initializer=HeUniform(), padding='same')(inputs)
    x = AveragePooling1D(pool_size=2, strides=2)(x)
    x = Flatten()(x)
    
    # 放寬瓶頸，使用 64 個神經元避免模式崩潰
    x = Dense(64, activation='selu', kernel_initializer=HeUniform())(x)
    outputs = Dense(256, activation='softmax')(x)
    
    model = Model(inputs, outputs)
    optimizer = Adam(learning_rate=1e-3)
    model.compile(loss='categorical_crossentropy', optimizer=optimizer, metrics=['accuracy'])
    return model

if __name__ == "__main__":
    print("[*] 載入資料中...")
    traces = np.load("data/cw_traces.npy")
    textins = np.load("data/cw_textins.npy")
    keys = np.load("data/cw_keys.npy")
    
    print("[*] 執行波形標準化 (防除零版本)...")
    mean_val = np.mean(traces, axis=0)
    std_val = np.std(traces, axis=0)
    std_val[std_val == 0] = 1 
    traces = (traces - mean_val) / std_val
    X = traces.reshape((traces.shape[0], traces.shape[1], 1))
    
    # 保留 20% 給最終攻擊驗證
    split = int(len(X) * 0.8)
    X_train, X_val = X[:split], X[split:]
    textins_train, keys_train = textins[:split], keys[:split]
    textins_val, keys_val = textins[split:], keys[split:]
    
    os.makedirs("data", exist_ok=True)

    print("[*] ====== 開始進行 16 Bytes 完整訓練 ======")
    for target_byte in range(16):
        print(f"\n========================================")
        print(f"[*] 正在訓練 Byte {target_byte} 的專屬模型")
        print(f"========================================")
        
        # 針對當前的 target_byte 產生 Label
        Y_train = np.zeros(len(X_train), dtype=int)
        Y_val = np.zeros(len(X_val), dtype=int)
        
        for i in range(len(X_train)):
            Y_train[i] = SBOX[textins_train[i][target_byte] ^ keys_train[i][target_byte]]
        for i in range(len(X_val)):
            Y_val[i] = SBOX[textins_val[i][target_byte] ^ keys_val[i][target_byte]]
            
        Y_train_categorical = to_categorical(Y_train, num_classes=256)
        Y_val_categorical = to_categorical(Y_val, num_classes=256)
        
        # 建立與訓練模型
        model = build_zaid_model(X.shape[1])
        model.fit(
            X_train, Y_train_categorical,
            batch_size=50,
            epochs=20,  # Zaid 模型收斂很快，20 個 Epoch 通常就很夠了
            validation_data=(X_val, Y_val_categorical),
            verbose=1
        )
        
        # 儲存該 Byte 的模型
        model_path = f"data/zaid_cw_model_byte{target_byte}.h5"
        model.save(model_path)
        print(f"[*] Byte {target_byte} 訓練完成！模型已儲存為 {model_path}")
