def build_zaid_model(input_length):
    """嚴格遵循 Zaid 論文 Table 9 針對 Synchronized Traces 的微型架構"""
    inputs = Input(shape=(input_length, 1))
    
    # Convolutional Block (強調 Kernel size = 1, Average Pooling)
    x = Conv1D(filters=2, kernel_size=1, activation='selu', kernel_initializer=HeUniform(), padding='same')(inputs)
    x = AveragePooling1D(pool_size=2, strides=2)(x)
    
    x = Flatten()(x)
    
    # Fully-Connected Block
    x = Dense(4, activation='selu', kernel_initializer=HeUniform())(x)
    outputs = Dense(256, activation='softmax')(x)
    
    model = Model(inputs, outputs, name="Zaid_Synchronized_CNN")
    optimizer = Adam(learning_rate=1e-3)
    model.compile(loss='categorical_crossentropy', optimizer=optimizer, metrics=['accuracy'])
    
    return model

if __name__ == "__main__":
    # 1. 載入資料
    print("[*] 載入資料中...")
    traces = np.load("data/cw_traces.npy")
    textins = np.load("data/cw_textins.npy")
    keys = np.load("data/cw_keys.npy")
    
    # 2. 資料預處理 (標準化 Z-Score Normalization)
    print("[*] 執行波形標準化 (防除零版本)...")
    mean_val = np.mean(traces, axis=0)
    std_val = np.std(traces, axis=0)
    
    # 將標準差為 0 的地方強制設為 1，避免發生除以零的 NaN 災難
    std_val[std_val == 0] = 1 
    
    traces = (traces - mean_val) / std_val
    
    # Reshape 給 Conv1D 吃: (Samples, Length, Channels)
    X = traces.reshape((traces.shape[0], traces.shape[1], 1))
    
    # 3. 標籤生成 (Target: 第一個 Byte 的 S-Box 輸出)
    print("[*] 生成 S-Box 標籤...")
    Y = np.zeros(len(traces), dtype=int)
    for i in range(len(traces)):
        Y[i] = SBOX[textins[i][0] ^ keys[i][0]]
    
    # One-hot encoding
    Y_categorical = to_categorical(Y, num_classes=256)
    
    # 切分訓練與驗證集 (80/20)
    split = int(len(X) * 0.8)
    X_train, X_val = X[:split], X[split:]
    Y_train, Y_val = Y_categorical[:split], Y_categorical[split:]
    
    # 4. 建立與訓練模型
    model = build_zaid_model(X.shape[1])
    model.summary()
    
    print("[*] ====== 開始訓練 ======")
    history = model.fit(
        X_train, Y_train,
        batch_size=50,      # Zaid 論文建議的 Batch size
        epochs=50,          
        validation_data=(X_val, Y_val),
        verbose=1
    )
    
    model.save("data/zaid_cw_model.h5")
    print("[*] 訓練完成！模型已儲存為 data/zaid_cw_model.h5")
